uicheckbox_release
		;jsr uielement_release

		ldy #$02
		jsr ui_getelementdata_2

		lda zpptr2+0						; get off or on
		beq :+
		lda #$00							; if on, store off/0
		ldy #$02
		sta (zpptr1),y
		bra :++

:		lda #$01							; if off, store on/1
		ldy #$02
		sta (zpptr1),y

:		jsr uicheckbox_draw
	   	rts

