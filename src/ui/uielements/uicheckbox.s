uicheckbox_draw

		jsr uidraw_set_draw_position

		ldy #$02
		jsr ui_getelementdata_2

		clc
		lda zpptr2+0
		adc #$04
		tay

		lda (zpptr1),y
		tay

		ldx #$00
		ldz #$00
		tya
:		sta [uidraw_scrptr],z
		inz
		inz
		adc #$01
		inx
		cpx uidraw_width
		bne :-
		
		rts

